def switch_it_up(number):
    if number == 1:
        number_str='One'
    elif number == 2:
        number_str='Two'
    elif number == 3:
        number_str='Three'
    elif number == 4:
        number_str='Four'
    elif number == 5:
        number_str='Five'
    elif number == 6:
        number_str='Six'
    elif number == 7:
        number_str='Seven'
    elif number == 8:
        number_str='Eight'
    elif number == 9:
        number_str='Nine'
    else:
        number_str = 'Zero'
    return number_str